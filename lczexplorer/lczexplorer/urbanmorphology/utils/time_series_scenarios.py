"""Scenario-driven LCZ time-series classification.

This module implements three classification workflows that produce annual LCZ
maps from Google Earth Engine assets.  Each scenario differs in how training
samples are obtained and how the final per-year results are assembled:

Scenario 1 – Yearly sample assets
    A dedicated training-sample asset exists for every target year.  Features
    are extracted from Landsat composites (≤ 2016) or the Google Satellite
    Embedding collection (> 2016), and a Random Forest classifier is trained
    independently for each year.  A focal-mode spatial filter is applied after
    classification.

Scenario 2 – Single-year sample with YOD-guided fusion
    A single training-sample asset (typically the most recent reliable year) is
    reused across all years.  For years ≤ 2016 a *hybrid* strategy is used:
    stable pixels (YOD = 0) are sampled from the fixed asset while *ghost*
    samples are drawn from the FROM-GLC Plus land-cover product in areas of
    detected change (YOD ≠ 0).  The final map is assembled by using the GLC
    guide as a base and replacing built-up pixels (lc_label = 8) with the RF
    result.  For years > 2016 only the stable samples are used and the RF
    result is taken directly.

Scenario 3 – No external samples; confidence-mask based
    No pre-labelled training assets are required.  Instead, a 2020 LCZ product
    is used as pseudo-labels.  A *confidence mask* is generated by running
    three independent RF classifiers on the 2020 embedding features and
    retaining only pixels where all three predictions agree with each other and
    with the pseudo-label.  The confidence mask is exported as a GEE asset and
    then used as the stable-area mask for the final per-year classification.
    Landsat years use a GLC-guided label fused with the 2020 product; built-up
    pixels (lc_label = 8) are replaced by the RF result.
"""

from __future__ import annotations

from typing import Dict, Iterable, List, Optional

import ee

from utils import config as Config
from utils.geemodules import ChangeDetector, LCZClassifier, TemporalAggregator


