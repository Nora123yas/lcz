def _get_landsat_features(year: int, city_id: int, region: ee.Geometry) -> ee.Image:
    """Return a Landsat feature image with spectral indices and NIR texture.

    Handles both Landsat 5/7 (SR_B1 … SR_B7) and Landsat 8/9 (SR_B2 … SR_B7)
    band layouts.  Falls back to legacy band names when neither layout matches.
    """
    from utils.processors import AssetManager

    asset_path = AssetManager.get_existing_asset_path(year, city_id)
    if not asset_path:
        raise ValueError(f"No Landsat asset found for city {city_id}, year {year}")

    img = ee.Image(asset_path).clip(region)
    bnames = img.bandNames()
    dst = ["blue", "green", "red", "nir", "swir1", "swir2"]

    base = ee.Image(ee.Algorithms.If(
        bnames.contains("SR_B6"),
        img.select(["SR_B2", "SR_B3", "SR_B4", "SR_B5", "SR_B6", "SR_B7"], dst),
        ee.Algorithms.If(
            bnames.contains("SR_B1"),
            img.select(["SR_B1", "SR_B2", "SR_B3", "SR_B4", "SR_B5", "SR_B7"], dst),
            img.select(["B1", "B2", "B3", "B4", "B5", "B7"], dst),
        ),
    ))

    ndvi = base.normalizedDifference(["nir", "red"]).rename("NDVI")
    ndbi = base.normalizedDifference(["swir1", "nir"]).rename("NDBI")
    mndwi = base.normalizedDifference(["green", "swir1"]).rename("MNDWI")
    nir_std = base.select("nir").reduceNeighborhood(
        reducer=ee.Reducer.stdDev(),
        kernel=ee.Kernel.square(3),
    ).rename("NIR_STD")

    return base.addBands([ndvi, ndbi, mndwi, nir_std])


def _get_embedding_features(year: int, region: ee.Geometry) -> Optional[ee.Image]:
    """Return a mosaicked Google Satellite Embedding image for *year*.

    Returns ``None`` when the collection is empty for the requested year.
    """
    collection_id = getattr(Config, "ALPHA_EARTH_COLLECTION", None)
    if not collection_id:
        return None

    start = ee.Date.fromYMD(year, 1, 1)
    end = ee.Date.fromYMD(year, 12, 31)

    col = (
        ee.ImageCollection(collection_id)
        .filterDate(start, end)
        .filterBounds(region)
    )
    # Use EE-side conditional to avoid pulling collection size to the client.
    # col.size().getInfo() on a large region can exceed the user memory limit.
    img = ee.Image(
        ee.Algorithms.If(
            col.size().gt(0),
            col.mosaic().clip(region),
            ee.Image(0),   # sentinel – detected below
        )
    )
    # Detect the sentinel without any .getInfo() call:
    # Check whether the first band is named 'constant' using a server-side
    # string comparison.  This avoids pulling any data to the client.
    first_band = img.bandNames().get(0)
    is_sentinel = ee.String(first_band).equals("constant")

    # Build the renamed image only when the collection was non-empty.
    # We use ee.Algorithms.If to keep everything server-side.
    bands = img.bandNames()
    renamed = img.select(
        bands,
        bands.map(lambda b: ee.String("EMB_").cat(ee.String(b))),
    ).clip(region)

    # Pull a single boolean to the client – negligible memory cost.
    try:
        if is_sentinel.getInfo():
            return None
    except Exception:
        return None

    return renamed


def _get_features_by_year(
    year: int,
    city_id: int,
    region: ee.Geometry,
) -> Optional[ee.Image]:
    """Select the appropriate feature image based on *year*.

    Uses Landsat for years ≤ 2016 and the Satellite Embedding for later years,
    falling back to Landsat when the embedding collection is unavailable.
    """
    if year <= 2016:
        return _get_landsat_features(year, city_id, region)

    emb = _get_embedding_features(year, region)
    if emb is not None:
        return emb

    # Graceful fallback: use Landsat even for post-2016 years
    return _get_landsat_features(year, city_id, region)


def _apply_smoothing(img: ee.Image, class_field: str, region: ee.Geometry) -> ee.Image:
    """Apply a circular focal-mode filter and clip to *region*."""
    return (
        img.focal_mode(radius=1.5, kernelType="circle", units="pixels", iterations=1)
        .toInt16()
        .rename(class_field)
        .clip(region)
    )


def _stratified_sample_n_per_class(
    sampling_img: ee.Image,
    region: ee.Geometry,
    n_per_class: int,
    seed: int,
    class_field: str,
    tile_scale: int = 4,
    geometries: bool = False,
) -> ee.FeatureCollection:
    """Stratified sample with a fixed quota per LCZ class (1–17)."""
    class_values = ee.List.sequence(1, 17)
    class_points = ee.List.repeat(n_per_class, 17)
    return sampling_img.stratifiedSample(
        numPoints=0,
        classBand=class_field,
        region=region,
        scale=Config.SCALE,
        seed=seed,
        classValues=class_values,
        classPoints=class_points,
        geometries=geometries,
        dropNulls=True,
        tileScale=tile_scale,
    )


